      private void CreatetimeComboBox() {
       
        date= dates.getSelectedItem().toString();
       
        BasicDBObject query = new BasicDBObject("topicName", topic);
        datesCur = MongoDB.topicsDB.find(query);
        
        SimpleDateFormat timeFormat = new SimpleDateFormat("HH:mm:ss");
        String t=null;
       
        SimpleDateFormat formatter = new SimpleDateFormat("dd-MM-yyyy");
        DBCursor d=datesCur;
        while(d.hasNext()){
            
            Date n = (Date) datesCur.next().get("Date");
            t = timeFormat.format(n);
            String k = formatter.format(n); 
            
            if(date.equals(k)){
            times.addItem(t);
            }        
       } 
            
    }      
