    private void topicsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_topicsActionPerformed
        datesCur=null;
         
        dates.removeAllItems();
        dates.addItem("");
        times.removeAllItems();
        topic= topics.getSelectedItem().toString();
        BasicDBObject query = new BasicDBObject("topicName", topic);
        datesCur = MongoDB.topicsDB.find(query);
        
        SimpleDateFormat formatter = new SimpleDateFormat("dd-MM-yyyy");
        String format=null;
        DBCursor j=datesCur;
        if(j.hasNext()){
        Date t = (Date) datesCur.next().get("Date");
        format = formatter.format(t);
        dates.addItem(format);
        } 
        
        while(datesCur.hasNext()){
            Date n = (Date) datesCur.next().get("Date");
            String d = formatter.format(n);
            if(!format.equals(d)){
             dates.addItem(d);
             format=d;
            }     
        }   
        datesCur = MongoDB.topicsDB.find(query);         // TODO add your handling code here:
    }//GEN-LAST:event_topicsActionPerformed

